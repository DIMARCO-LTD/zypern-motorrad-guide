/* ===== INIT ===== */
document.addEventListener('DOMContentLoaded', () => {
  // Wire up language buttons
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.addEventListener('click', () => switchLang(btn.getAttribute('data-lang')));
  });

  applyTranslations();
  initNavbar();
  initActiveNav();
  initScrollAnimations();
  initVideo();
});
