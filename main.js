/* ===== LANGUAGE STATE ===== */
let currentLang = localStorage.getItem('lang') || 'de';

function t(key) {
  return i18n[currentLang][key] || i18n['de'][key] || key;
}

function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const val = t(key);
    if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
      el.placeholder = val;
    } else {
      el.innerHTML = val;
    }
  });

  // Update lang buttons
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.getAttribute('data-lang') === currentLang);
  });

  // Update html lang attribute
  document.documentElement.lang = currentLang;

  // Update difficulty dots
  updateDifficultyDots();
}

function updateDifficultyDots() {
  document.querySelectorAll('[data-difficulty]').forEach(container => {
    const level = parseInt(container.getAttribute('data-difficulty'));
    const max = 5;
    container.innerHTML = '';
    for (let i = 1; i <= max; i++) {
      const dot = document.createElement('span');
      dot.className = 'dot' + (i <= level ? ' filled' : '');
      container.appendChild(dot);
    }
  });
}

function switchLang(lang) {
  currentLang = lang;
  localStorage.setItem('lang', lang);
  applyTranslations();
}

/* ===== NAVBAR SCROLL ===== */
function initNavbar() {
  const navbar = document.querySelector('.navbar');
  if (!navbar) return;

  const onScroll = () => {
    navbar.classList.toggle('scrolled', window.scrollY > 40);
  };
  window.addEventListener('scroll', onScroll, { passive: true });
  onScroll();

  // Hamburger
  const hamburger = document.querySelector('.hamburger');
  const nav = document.querySelector('.navbar-nav');
  if (hamburger && nav) {
    hamburger.addEventListener('click', () => {
      nav.classList.toggle('open');
      hamburger.classList.toggle('open');
    });

    // Close on link click
    nav.querySelectorAll('.nav-link').forEach(link => {
      link.addEventListener('click', () => {
        nav.classList.remove('open');
        hamburger.classList.remove('open');
      });
    });
  }
}

/* ===== ACTIVE NAV LINK ===== */
function initActiveNav() {
  const path = window.location.pathname.split('/').pop() || 'index.html';
  document.querySelectorAll('.nav-link').forEach(link => {
    const href = link.getAttribute('href') || '';
    const isHome = (href === 'index.html' || href === './') && (path === 'index.html' || path === '');
    const matches = href && path.includes(href) && href !== 'index.html';
    link.classList.toggle('active', isHome || matches);
  });
}

/* ===== SCROLL ANIMATIONS ===== */
function initScrollAnimations() {
  if (!('IntersectionObserver' in window)) return;

  const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        entry.target.classList.add('visible');
        observer.unobserve(entry.target);
      }
    });
  }, { threshold: 0.1, rootMargin: '0px 0px -40px 0px' });

  document.querySelectorAll('.fade-up').forEach(el => observer.observe(el));
}

